/**
 * @abstraction:
 * 	- Main entry of the power supply.
*/
#include <alinix/types.h>
#include <alinix/device.h>
#include <alinix/pm.h>

static bool pm_ops_is_empty(const struct dev_pm_ops *ops)
{
	if (!ops)
		return true;

	return !ops->prepare &&
	       !ops->suspend &&
	       !ops->suspend_late &&
	       !ops->suspend_noirq &&
	       !ops->resume_noirq &&
	       !ops->resume_early &&
	       !ops->resume &&
	       !ops->complete;
}